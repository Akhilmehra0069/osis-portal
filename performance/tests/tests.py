from django.test import TestCase
from admission.tests import data_for_tests
from performance.models import student_performance
import datetime


class TestStudentPerformance(TestCase):
    def setUp(self):
        self.student_performance = data_for_tests.create_student_performance()

    def test_has_expired(self):
        timedelta = datetime.timedelta(days=4)

        self.assertFalse(student_performance.has_expired(self.student_performance))

        self.student_performance.expiration_date = datetime.date.today() + timedelta
        self.assertFalse(student_performance.has_expired(self.student_performance))

        self.student_performance.expiration_date = datetime.date.today() - timedelta
        self.assertTrue(student_performance.has_expired(self.student_performance))


